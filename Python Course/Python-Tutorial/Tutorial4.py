# 2. conditional operators:- Used to compare values :-  
# operator Name  =  (operator symbol)  (example)
# Equal to       =  (==)                 (a==b)
# Not equal to   =  (!=)                 (a!=b)
# Greater than   =  (>)                  (a>b)
# Less than      =  (<)                  (a<b)
# Greater than or equal to = (>=)          (a>=b)
# Less than or equal to    = (<=)          (a<=b)
# Example of conditional operators in python :-
a = 5
b = 10
print(a==b)
print(a!=b)
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)
